package datatype;

public class DataTypePractice {

    public static void main(String[] args) {
        // data_type variable_name = value;

        int numericalData = 2;
        numericalData = 5;

        final double PI_VALUE;
        PI_VALUE = 3.14;

        //PI_VALUE = 2.14;// cannot do this

        //Integer type
        byte b = -128;
        short s = 32000;
        int i = 2000000000;
        long l = 987654321034345657L;

        // Floating point Type
        float f = 2.14F;
        double lat = 27.1234567890123456;

        System.out.println(lat);

        // Character Type
        char c = 'a';

        //Boolean Type
        boolean isActive = false;

        // String (NOT PRIMITIVE TYPE)
        String name = "Java Class";
    }
}
